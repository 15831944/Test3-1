/*!!!!!!!!                                                      !!!!!!!!*/
/*!!!!!!!!(C) Copyright 1989, 1990, 1991 Building Block Software!!!!!!!!*/
/*!!!!!!!!                   All rights reserved                !!!!!!!!*/
/*!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!*/
PT2 min_pt, max_pt ;
{
    C2_BOX box ;

    box = (C2_BOX)dmm_malloc ( sizeof ( C2_BOX_S ) ,"C2_BOX_S") ;
    if ( box == NULL ) 
        RETURN ( NULL ) ;
    C2V_COPY ( min_pt, C2_MIN_PT(box) ) ;
    C2V_COPY ( max_pt, C2_MAX_PT(box) ) ;
    RETURN ( box ) ;
}


