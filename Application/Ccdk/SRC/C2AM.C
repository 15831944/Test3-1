void c2a_rotate_pt_cs ( a0, n, origin, c, s, a1 ) 
/*-------------------------------------------------------------------------*/
/*!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!*/
/*!!!!!!!!                                                          !!!!!!!*/
/*!!!!!!!! (C) Copyright 1989, 1990, 1991 Building Block Software!!!!!!!!*/
/*!!!!!!!!                    All rights reserved                   !!!!!!!*/
/*!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!*/
PT2    *a0, *a1 ;
INT     n ;
PT2     origin ;
REAL    c, s ;
{
    INT i ;
    PT2 vec ;

    for ( i=0 ; i<n ; i++ ) {
        C2V_SUB ( a0[i], origin, vec ) ;
        C2V_ROTATE_VEC_CS( vec, c, s, a1[i] ) ;
        C2V_ADD ( a1[i], origin, a1[i] ) ;
    }
}
