HPT3 *a ;
INT n ;
{

    INT i ;

    for ( i=0 ; i<n ; i++ ) 
        if ( !IS_ZERO ( a[i][3]-1.0 ) ) 
            RETURN ( FALSE ) ;
    RETURN ( TRUE ) ;
}
